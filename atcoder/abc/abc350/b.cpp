#include <bits/stdc++.h>
#define _ ios_base::sync_with_stdio(0);cin.tie(0);
#define endl '\n'
#define all(x) (x).begin(), (x).end()
#define rall(x) (x).rbegin(), (x).rend()
typedef long long ll;
using namespace std;
#define int ll

int solve() {
	int n, q; cin >> n >> q;
	vector<int> A(n, 1);
	for (int i = 0, p; i < q; i++) {
		cin >> p; p--;
		A[p] ^= 1;
	}
	cout << count(all(A), 1) << endl;

	return(0);
}

