int solve() {
	int n; cin >> n;
	int p = n;

	for (int i = 1; i < n; i++) {
		p = (i & 1 ? p - (n - i) : p + (n - i));
	}
	cout << p << endl;

	return(0);
}

int main()
{
    _;

	int t = 1; cin >> t;
	while (t--) {
		solve();
	}
    
    return(0);
}

