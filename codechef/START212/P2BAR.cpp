int solve() {
	int n, res = 0; cin >> n;
	string S; cin >> S;

	for (int i = 0, vi = 0; i < n; i++) {
		vi |= S[i] == '0';
		res += vi and S[i] == '1';
	}
	cout << res << endl;

	return(0);
}

