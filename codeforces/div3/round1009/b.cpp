using namespace std;

void solve() {
	int n; cin >> n;
	vector<int> v(n);
	for (int& i : v) cin >> i;

	if (n == 1) {
		cout << v[0] << endl;
		return;
	}
	int agr = v[0] + v[1] - 1;
	for (int i = 2; i < n; i++) {
		agr = agr + v[i] - 1;
	}
	cout << agr << endl;
}

int main()
{
    _;

	int t; cin >> t;
	while (t--) {
		solve();
	}
    
    return(0);
}
